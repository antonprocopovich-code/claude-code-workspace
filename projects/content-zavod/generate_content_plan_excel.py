    cell.font = font_week()
    cell.fill = fill(COLOR_WEEK)
    cell.alignment = Alignment(horizontal="left", vertical="center", indent=1)
    ws.row_dimensions[row].height = 20
    row += 1

    for post in posts:
        num, date, platform, hook, ptype, pillar, funnel, cta, fmt = post
        row_data = [num, date, platform, hook, ptype, pillar, funnel, cta, fmt]
        bg = TYPE_COLORS.get(ptype, "FFFFFF")
        ws.row_dimensions[row].height = 45

        for col_idx, value in enumerate(row_data, start=1):
            cell = ws.cell(row=row, column=col_idx, value=value)
            cell.font = font_body()
            cell.fill = fill(bg)
            cell.border = thin_border()
            wrap = col_idx in (3, 4, 8, 9)
            halign = "center" if col_idx in (1, 2, 5, 6, 7) else "left"
            cell.alignment = Alignment(horizontal=halign, vertical="center", wrap_text=wrap)

        row += 1

